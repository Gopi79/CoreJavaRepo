package com.patternpro;

public class Pattern4 
{
	public static void main(String[] args) 
	{
		for(int i=5;i>=1;i--)
		{
			for(int j=1;j<=i;j++)
			{
				System.out.print(j);
			}
			System.out.println();
		}
		for(int g=1;g<=5;g++)
		{
			for(int h=1;h<=g;h++)
			{
				System.out.print(h);
			}
			System.out.println();
		}
	}
}

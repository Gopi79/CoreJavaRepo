package com.patternpro;

public class Pattern5 
{
	public static void main(String[] args) 
	{
		for(int i=5;i>=1;i--)
		{
			for(int j=i;j>=1;j--)
			{
				System.out.print(j);
			}
			System.out.println();
		}
		for(int a=1;a<=5;a++)
		{
			for(int b=a;b>=1;b--)
			{
				System.out.print(b);
			}
			System.out.println();
		}
	}
}

package com.calc;
public class Calculator 
{
	int addition(int x,int y)
	{
		int i=x;
		int j=y;
		return i+j;
	}
	int substraction(int x,int y)
	{
		int i=x;
		int j=y;
		return i-j;
	}
	int multiplication(int x,int y)
	{
		int i=x;
		int j=y;
		return i*j;
	}
	int division(int x,int y)
	{
		int i=x;
		int j=y;
		return i/j;
	}
}
